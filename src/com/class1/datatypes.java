package com.class1;

public class datatypes {

	public static void main(String[] args) {
		// Creating variables 
 		byte b=-128;
 		short s=3276; 
 		int i=678769;
 		
 		//numbers with decimals
 		float f=2.2f;
 		double d=3.7685665465;
 		
 		//character
 		char c='a';
 		
 		//boolean (true or false)
 		boolean g=false;
 		
 		//string
 		String str="A123i";

 		System.out.println(g);
 		
 		
	}

}
